#include<iostream>
using namespace std ;
int main(){
    int x = 5;
    cout<<x<<endl;
    x++; // post increment
    cout<<x<<endl;
    ++x; //pre increment
    cout<<x<<endl;
    x--; //post decrement
    cout<<x<<endl;
    --x; //pre decrement
    cout<<x<<endl;
    cout<<++x<<endl;
    cout<<x++<<endl;
    cout<<--x<<endl;
    cout<<x--<<endl;
    cout<<x<<endl;
    

}